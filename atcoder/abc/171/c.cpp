// dump
//--------------------------------------------
#define DUMPOUT cerr

void dump_func() { DUMPOUT << endl; }
template <class Head, class... Tail>
void dump_func(Head &&head, Tail &&... tail) {
    DUMPOUT << head;
    if (sizeof...(Tail) > 0) {
        DUMPOUT << ", ";
    }
    dump_func(std::move(tail)...);
}
#ifdef DEBUG_
#define DEB
#define dump(...)                                                       \
    DUMPOUT << "  " << string(#__VA_ARGS__) << ": "                     \
            << "[" << to_string(__LINE__) << ":" << __FUNCTION__ << "]" \
            << endl                                                     \
            << "    ",                                                  \
        dump_func(__VA_ARGS__)
#else
#define DEB if (false)
#define dump(...)
#endif

LL n;

int main(int argc, char *argv[]) {
    cin >> n;
    LL base = 0;
    LL beforeBase;
    int mulCnt = 0;
    while (base < n) {
        beforeBase = base;
        mulCnt++;
        base += pow(26, mulCnt);
    }

    n -= beforeBase;
    dump(n);
    dump(mulCnt);

    bool pad = false;
    for (int i = mulCnt - 1; i >= 0; i--) {
        int e = n / (LL)pow(26, i);
        cout << e << endl;
        if (e == 0 && !pad) {
            cout << 'a';
        } else if (e == 0 && pad) {
            cout << 'z';
        } else {
            cout << (char)('a' + e - 1);
            pad = true;
        }
        n = n % (LL)pow(26, i);
    }
    cout << endl;
    return 0;
}
