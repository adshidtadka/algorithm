int n, m;
int x[100010];

bool isCheck(LL minute) {
    LL p = 0;
    REP(i, m) {
        int dist = x[i] - p;
        if (dist > minute) {
            return false;
        } else if (dist> 0) {
            p = max(minute - dist *2, (minute - dist)/2) + x[i] + 1;
        } else {
            p = max(p, x[i] + minute + 1);
        }
    }
    return p >= n;
}

int main(int argc, char *argv[]) {
    cin >> n >> m;
    REP(i, m) {
        cin >> x[i];
        x[i]--;
    }
    LL left = 0;
    LL right = n * 2;
    LL mid;
    while (right - left > 1) {
        mid = (right + left) / 2;
        if (isCheck(mid)) {
            right = mid;
        } else {
            left = mid;
        }
    }
    if (isCheck(left)) {
        cout << left << endl;
    } else {
        cout << right << endl;
    }
    return 0;
}
